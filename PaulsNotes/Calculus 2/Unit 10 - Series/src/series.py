    def construct(self):

        exampleSeries = MathTex(r"\sum_{i=1}^{\infty} = \frac{3}{i^2+1}")

        self.wait(2)
        self.play(Write(exampleSeries))
        self.wait(2)
        self.play(FadeOut(exampleSeries))

